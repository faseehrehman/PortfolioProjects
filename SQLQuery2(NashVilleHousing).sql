---Remove Duplicates

WITH RowNumCTE AS(
SELECT *,
row_number() OVER (
	PARTITION BY ParcelID,
	PropertyAddress, 
	SalePrice, 
	SaleDate, 
	LegalReference
	ORDER BY
		UniqueID
		) row_num


FROM PortfolioProject.dbo.NashvilleHousing

)

Delete
from RowNumCTE
WHERE row_num > 1
order by PropertyAddress


